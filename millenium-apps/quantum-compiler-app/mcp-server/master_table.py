def get_by_name(name: str) -> dict:
    """Look up a formula by observable name."""
    for entry in MASTER_TABLE:
        if entry["name"] == name:
            return entry
    raise KeyError(f"Unknown observable: {name}")


def get_by_sector(sector: str) -> list:
    """Get all formulas in a sector (A/B/C/D)."""
    return [e for e in MASTER_TABLE if e["sector"] == sector]


def get_by_rule(rule) -> list:
    """Get all formulas using a given grammar rule."""
    return [e for e in MASTER_TABLE if e["rule"] == rule]


def get_by_register(n: int) -> list:
    """Get all formulas using register γ_n."""
    return [e for e in MASTER_TABLE if n in e.get("operands", [])]
